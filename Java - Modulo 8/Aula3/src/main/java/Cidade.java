public class Cidade {
    private long id;
    private String Nome;
    private long IDEstado;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getNome() {
        return Nome;
    }

    public void setNome(String Nome) {
        this.Nome = Nome;
    }

    public long getIDEstado() {
        return IDEstado;
    }

    public void setIDEstado(long IDEstado) {
        this.IDEstado = IDEstado;
    }
}
